package elaborazioneMaglia;

public class TrasportoCaduta {
	
	private int rigaDisegno;
	
	private int posizione;
	
	private String avantiDietro;
	
	private String dietroAvanti;
	
	public TrasportoCaduta() {}

	public int getRigaDisegno() {
		return rigaDisegno;
	}

	public void setRigaDisegno(int rigaDisegno) {
		this.rigaDisegno = rigaDisegno;
	}

	public int getPosizione() {
		return posizione;
	}

	public void setPosizione(int posizione) {
		this.posizione = posizione;
	}

	public String getAvantiDietro() {
		return avantiDietro;
	}

	public void setAvantiDietro(String avantiDietro) {
		this.avantiDietro = avantiDietro;
	}

	public String getDietroAvanti() {
		return dietroAvanti;
	}

	public void setDietroAvanti(String dietroAvanti) {
		this.dietroAvanti = dietroAvanti;
	}
	

}
